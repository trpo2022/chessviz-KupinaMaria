#include <lib/vertical.h>
int vertical(char x)
{
    if (x == 'a')
        return 1;
    else if (x == 'b')
        return 2;
    else if (x == 'c')
        return 3;
    else if (x == 'd')
        return 4;
    else if (x == 'e')
        return 5;
    else if (x == 'f')
        return 6;
    else if (x == 'g')
        return 7;
    else if (x == 'h')
        return 8;
    else
        return -1;
}